void invalidateCommStatemachine() {
    for (int i=0;i<inputStringSize+1;i++)
        inputString[i]=0;
    inputStringPos=0;

    COMMLOG("invalidateCommStatemachine()");
    xSemaphoreTake(CommMutex, portMAX_DELAY);
        UnlockChallengeValid=0;
        CureBaseUnlocked=0;
    xSemaphoreGive(CommMutex);
}

void CommRxTask(void *Param) {
    COMMLOG("Entering CommRxTask\n");
    curve=uECC_secp256k1();
    invalidateCommStatemachine();

    uECC_set_rng(GetRandomData);

    memset(&MiniZStream, 0, sizeof(mz_stream));

    MiniZStream.next_in = 0;
    MiniZStream.avail_in = 0;
    MiniZStream.next_out = FlashBuffer;
    MiniZStream.avail_out = FlashBufferSize;

    mz_inflateInit(&MiniZStream);

    led_setAdvertising();
    
  //  esp_task_wdt_reset();
 //   esp_err_t ret;
  //  esp_task_wdt_add(xTaskGetCurrentTaskHandle());
  //  esp_task_wdt_reset();

    int idleCounter=100;
    while(1) {
        char byte;
        if (xQueueReceive(RxQueueHandle,&byte,100 / portTICK_PERIOD_MS)==pdTRUE) {
            idleCounter=100;
            //new data? let's switch to snappy connection parameters
            if (CureBaseUnlocked)
                updateConnectionParameters(0);
            //COMMLOG("Processing RxByte \"%c\"", byte);

        //    esp_task_wdt_reset();
            CommStringParserStateMachine(byte);
         //   esp_task_wdt_reset();

            //taskYIELD();

        } else {
         //   esp_task_wdt_reset();

            if (idleCounter>0)
                idleCounter--;
            if (idleCounter==0) {
                //10s of inactivity lets us switch to relaxed connection parameters.
                if (CureBaseUnlocked)
                    updateConnectionParameters(1);
            }
        }
    }
}

